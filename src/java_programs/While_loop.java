public class While_loop {
/*it verifies condition first,if condition is true it executes statement
by going to loop, it comes out of the loop when condition is false*/
	public static void main(String[] args) {
           int i =0;
           while(i<3) {
        	   
        	   System.out.println("the value of i is "+i);
        	   i++;
           }
	}

}

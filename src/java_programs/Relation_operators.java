package java_programs;

public class Relation_operators {

	public static void main(String[] args) {
       //relational operators
		int a=3, b=8, c=3, d=6;
		System.out.println(a==b);
		System.out.println(a==c);
		System.out.println(a!=b);
		System.out.println(a!=c);
		System.out.println(a>b);
		System.out.println(a<c);
		System.out.println(b>d);
		System.out.println(a>=c);
		System.out.println(a<=d);
	}

}
